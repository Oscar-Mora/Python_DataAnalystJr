# Construya un programa tal que dado como dato la calificación de un alumno en un 
# examen, escriba “aprobado” si su calificación es mayor o igual a 6 y “reprobado” en 
# caso contrario.


calif = float(input('Ingrese su calificación final: '))

if calif >= 6:
    print('APROBADO')
else:
    print('REPROBADO')
